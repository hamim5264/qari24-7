  void changeLocale(String lang) async {
    final index = langs.indexOf(lang);
    if (index != -1) {
      Get.updateLocale(locales[index]);
      currentLanguage.value = lang;
      final prefs = await SharedPreferences.getInstance();
      await prefs.setString('lang', lang);
    }
  }
